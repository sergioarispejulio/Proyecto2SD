    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int i = 1;
        lista.clear();
        try 
        {
            lista = cone.obtenerfrutas(jTextField1.getText(), (float)jSpinner1.getValue(), (float)jSpinner2.getValue());
            for (Producto producto : lista) {
                list2.add(Integer.toString(i));
                list3.add(producto.Nombre_Producto);
                list4.add(producto.Compania);
                list5.add(Integer.toString(producto.Cantidad));
                int aux = (int)producto.Costo;
                list6.add(Integer.toString(aux));
                aux = (int)producto.Costo_Envio;
                list7.add(Integer.toString(aux));
                i = i+1;
            }
        }// TODO add your handling code here:
        catch ( Exception e )
        {
           System.out.println("Exception: " + e.getMessage());
           e.printStackTrace();
           return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        int i=0;
        Producto nue = new Producto();
        for (Producto producto : lista) {
                if(i == Integer.parseInt(jTextField2.getText()))
                {
                    nue = producto;
                    break;
                }
                i = i+1;
        }
        try {
            cone.comprarfruta(nue.Nombre_Producto, nue.Compania, nue.Costo, nue.Cantidad, nue.Costo_Envio);
        } catch (RemoteException ex) {
            Logger.getLogger(CompradorFrutas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button1ActionPerformed

